using UnityEngine;

public class PersistentRoot : MonoBehaviour
{
    public static PersistentRoot Instance { get; private set; }
    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }

        Instance = this;
        DontDestroyOnLoad(gameObject);
    }
}
